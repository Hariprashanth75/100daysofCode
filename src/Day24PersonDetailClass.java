
public class Day24PersonDetailClass {

	String name;
	int age;
	boolean enrolled;
	public Day24PersonDetailClass(String name, int age, boolean enrolled)
	{
		this.name=name;
		this.age=age;
		this.enrolled=enrolled;
	}
	
	public String getName()
	{
		return name;
	}
	
	public int getAge()
	{
		return age;
	}
	
	public boolean isEnrolled()
	{
		return enrolled;
	}

}

